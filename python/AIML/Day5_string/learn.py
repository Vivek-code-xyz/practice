text = "Vivek Khasiya"

#slicing
print(text[::-1])  #it reverses the string 
print(text[0:6]) # slice it like an arrray
print(text[0:8:2])  #slice with every 2nd char only---> 0 2 4 6

print(text[:]) #prints whole string
print(text[-1 : :-1]) #reverses the string again
print(text[7::-1])   #reverses the part of the string
