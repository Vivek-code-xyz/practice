import math

# Square root - for distances, standard deviations
math.sqrt(16)  # Returns 4.0

# Power - for exponentiation
math.pow(2, 3)  # Returns 8.0 (2³)

# Logarithms
math.log(10)      # Natural log (base e)
math.log10(100)   # Base-10 log, returns 2.0
math.log2(8)      # Base-2 log, returns 3.0

# Exponential
math.exp(2)       # e², returns 7.389...

# Constants
math.pi           # 3.14159...
math.e            # 2.71828...